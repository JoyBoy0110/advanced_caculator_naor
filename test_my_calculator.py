import pytest
from main import cal

@pytest.mark.parametrize("_input, result", [
    #sintax

    #gibrish
    ("sdfghjkl___   ", 1),
    #empty
    ('',1),
    #easy
    ("1+2/2", 2.0),
    ("0.15 * 2 + 0.7", 1.0),
    ("2^3 / (10 / 32# + 6)", 1.0),
    ("1+2/2", 2.0),
    ("15 + 30-~2", 47.0),
    ("2^3 / (10 / 32# + 6)", 1.0),
    ("1+2/2", 2.0),
    ("15 * ~2 + 30", 0.0),
    ("2^3 / (10 / 32# + 6)", 1.0),
    ("((((~-3!!^~-3!)#/5) ^ 100)#!#)", 65.0),
    #complex
    ("1+2/2*50!#+(16^(1/2)-5!+4!)", -37.0),
    ("((15 * ~2) / (3^2+1)) + 4! % 135#", 3.0),
    ("(2^3 * ~5) + (10 / 32#) - 7", -45.0),
    ("((((~-3!!^~-3!)#/5) ^ 100)#!#)", 65.0)
])
def test_mytest(_input, result):
    assert cal(_input) == result